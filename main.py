@app.route("/post/<int:post_id>", methods=["GET", "POST"])
def show_post(post_id):
    requested_post = db.session.query(BlogPost).filter_by(id=post_id).first()
    form = CommentForm()
    if form.validate_on_submit():
        if not current_user.is_authenticated:
            flash("You need to be logged in to post a comment, please log in!")
            return redirect(url_for("login"))
        new_comment = Comment()
        new_comment.text = request.form["comment"]
        new_comment.comment_author = current_user
        new_comment.parent_post = requested_post
        db.session.add(new_comment)
        db.session.commit()
        form = CommentForm(formdata=None)
    return render_template("post.html", post=requested_post, form=form, current_user=current_user)


