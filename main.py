@app.route('/register', methods=["GET", "POST"])
def register():
    form = RegisterForm()
    if form.validate_on_submit():
        if request.method == "POST":
            user = User()
            user.email = request.form["email"]
            user.password = generate_password_hash(request.form["password"], method="pbkdf2:sha256", salt_length=16)
            user.name = request.form["name"]
            user_exist = db.session.query(User).filter_by(email=user.email).first()
            if not user_exist:
                db.session.add(user)
                db.session.commit()
                login_user(user)
                return redirect(url_for("get_all_posts"))
            else:
                flash("The email provided is already registered, please log in!", category="error")
                return redirect(url_for("login"))
        else:
            return render_template("register.html", form=form)
    return render_template("register.html", form=form)


@app.route('/login', methods=["GET", "POST"])
def login():
    form = LoginForm()
    if form.validate_on_submit():
        email = request.form["email"]
        password = request.form["password"]
        user = db.session.query(User).filter_by(email=email).first()
        if user:
            if not check_password_hash(user.password, password):
                flash("Incorrect password, please try again.", category="error")
                return render_template("login.html", form=form)
            login_user(user)
            return redirect(url_for("get_all_posts"))
        flash("The email provided does not exist, please register.", category="error")
        return render_template("login.html", form=form)
    return render_template("login.html", form=form)


